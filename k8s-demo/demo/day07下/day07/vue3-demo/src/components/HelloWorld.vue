  name: 'HelloWorld',
  // 使用props去声明子组件中，父传子的参数
  props: {
    msg: String
  },
  //声明emits事件
  emits: ['childMsg'],
  //pros是获取父传子的相关数据
  //ctx上下文，这里用到了emit事件去处理子传父的数据
  setup(props, ctx) {
    //props.msg
    const content = ref('你好，这里是子组件的数据')
    function btn() {
      //通过ctx的emit方法，注册一个childMsg事件，将content数据传出去
      ctx.emit("childMsg", content.value, '123')
    }
    return {
      btn
    }
  }
